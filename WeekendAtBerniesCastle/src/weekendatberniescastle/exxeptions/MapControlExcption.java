public class MapControlExcption extends Exception{

    public MapControlExcption() {
    }

    public MapControlExcption(String message) {
        super(message);
    }

    public MapControlExcption(String message, Throwable cause) {
        super(message, cause);
    }

    public MapControlExcption(Throwable cause) {
        super(cause);
    }

    public MapControlExcption(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
    
    
    
    
    
    
    
}
