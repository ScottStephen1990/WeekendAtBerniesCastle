public class PuzzleContorlException extends Exception {

    public PuzzleContorlException() {
    }

    public PuzzleContorlException(String message) {
        super(message);
    }

    public PuzzleContorlException(String message, Throwable cause) {
        super(message, cause);
    }

    public PuzzleContorlException(Throwable cause) {
        super(cause);
    }

    public PuzzleContorlException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    
}
