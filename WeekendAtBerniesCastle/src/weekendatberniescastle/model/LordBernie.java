package weekendatberniescastle.model;

/**
 *
 * @author Phillip
 */
public class LordBernie {
    
    int health;
    int defense;
    int attack;
    

    @Override
    public int hashCode() {
        int hash = 7;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final LordBernie other = (LordBernie) obj;
        return true;
    }

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getDefense() {
        return defense;
    }

    public void setDefense(int defense) {
        this.defense = defense;
    }

    public int getAttack() {
        return attack;
    }

    public void setAttack(int attack) {
        this.attack = attack;
    }

    public LordBernie() {
        this.attack = 3;
        this.defense = 5;
        this.health = 50;
        
    }
  
    
}
